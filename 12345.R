# 創建 x 軸座標值，從-4到4建立1000個均勻分布的點
x <- seq(-4, 4, length.out = 1000)

# 計算對應的 y 值，即機率密度函數值
# f(x) = (1/√(2π))2^(-x^2/2)
y <- (1/sqrt(2*pi)) * 2^(-x^2/2)

# 繪製主圖
plot(x, y, type = "l", # type="l"表示畫線
     lwd = 2,          # lwd=2設定線寬
     xlab = "x",       # x軸標籤
     ylab = "f(x)",    # y軸標籤
     main = "f(x) = (1/√(2π))2^(-x^2/2)") # 圖的標題

# 添加參考線
abline(h = 0, v = 0,    # h=0是水平線，v=0是垂直線
       lty = 2,         # lty=2設定虛線樣式
       col = "gray")    # 設定線的顏色為灰色



	